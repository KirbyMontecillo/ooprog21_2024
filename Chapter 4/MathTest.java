
class MathTest
{
   public static void main (String [] args)
   {
      System.out.println("SQRT: " + Math.sqrt(37));
      System.out.println("SIN: " + Math.sin(5));
      System.out.println("COS: " + Math.cos(13));
      System.out.println("FLOOR: " + Math.floor(8));
      System.out.println("CEIL: " + Math.ceil(6));
      System.out.println("ROUND: " + Math.cos(4));
      System.out.println("MAX: " + Math.cos(1,9));
      System.out.println("MIN: " + Math.cos(4,7));
      System.out.println("RANDOM: " + Math.random());
   }
}
